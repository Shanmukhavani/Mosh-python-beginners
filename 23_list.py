#lists are mutable
names = ['John' , 'Bob' , 'Mosh' , 'Sarah' ,'Mary']
print(names) #output : ['John', 'Bob', 'Mosh', 'Sarah', 'Mary']

print(names[0]) #John
print(names[2]) #Mosh
print(names[-1])#Mary
print(names[2:4]) #['Mosh', 'Sarah']
print(names[1:]) #['Bob', 'Mosh', 'Sarah', 'Mary']
print(names[:])#['John', 'Bob', 'Mosh', 'Sarah', 'Mary']